    static boolean[][] get(int[] data){
        boolean[][] arr = new boolean[16][8];
        int byteCount;
        int lCount;
        byteCount = 0;
        for (int line = 0; line < all_16_32; line++) {
            lCount = 0;
            for (int j = 0; j < 8; j++) {
                if (((data[byteCount] >> (7 - j)) & 0x1) == 1) {
                    arr[line][lCount] = true;
                } else {
                    arr[line][lCount] = false;
                }
                lCount++;
            }
            byteCount++;
        }
        return arr;
    }


